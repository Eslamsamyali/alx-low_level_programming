/**
 * _islower - function to check if
 * character is lowercase
 *
 * Desccription: function that checks for lowercase character.
 *
 * @C: check input of function
 *
 * Return: returns 1 if `c` is lowercase
 * otherwise always 0 (Success)
*/

int _islower(int C)
{
	if (c >= 97 && c <= 122)
		return (1);
	return (0);
}
